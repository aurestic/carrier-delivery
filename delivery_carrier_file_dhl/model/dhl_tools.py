def dhl_format_zip(country_code, zipcode):
    zipcode = zipcode.replace(' ', '').replace('-', '')
    if country_code == 'PT':
        zipcode = zipcode[0:4]
    if country_code == 'AD':
        zipcode = "25999"
    if country_code == "GI":
        zipcode = "11999"
    return zipcode
